function calculateBAS(p) {
  let score = 0;
  score += (p.player_kills * 10);
  score += (p.total_mob_kills * 2);
  score += (p.blocks_mined * 0.01);
  score -= p.deaths;
  return Math.max(0, Math.round(score));
}

function detectPlaystyle(p) {
  if (p.player_kills > 50) return "Executioner | PvP Legend";
  if (p.total_mob_kills > 5000) return "Slayer | Monster Hunter";
  if (p.blocks_mined > 50000) return "Miner | Deepslate Devourer";
  return "Adventurer | Newcomer";
}

